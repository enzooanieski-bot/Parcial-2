print('cálculo da área de um triângulo')
# mensagem que diz oque o programa faz

base = float(input("Digite a base do triângulo: "))
# pede ao usuário o tamanho da base do triângulo e o salva na variável 'base'
# input recebe como texto e float converte para número decimal

altura = float(input("Digite a altura do triângulo: "))
# pede ao usuário a altura do triângulo e a salva na variável 'altura'
# input recebe como texto e float converte para número decimal

área = (base * altura) / 2
# utiliza a fórmula que calcula a área de um triângulo e salva o resultado na variável 'área'

print("A área do triângulo é:", área)
# mensagem com o resultado da área

